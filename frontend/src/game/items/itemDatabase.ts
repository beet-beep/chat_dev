// ê°€ì±  ë“œë¡­ ê³„ì‚°
export function calculateGachaDrop(): GameItem {
  const roll = Math.random() * 100;
  let cumulative = 0;

  // ë“±ê¸‰ë³„ í™•ë¥  ê³„ì‚°
  const rarities: ItemRarity[] = ["mythic", "legendary", "epic", "rare", "common"];
  let selectedRarity: ItemRarity = "common";

  for (const rarity of rarities) {
    cumulative += RARITY_CONFIG[rarity].dropRate;
    if (roll < cumulative) {
      selectedRarity = rarity;
      break;
    }
  }

  // í•´ë‹¹ ë“±ê¸‰ ì•„ì´í…œ ì¤‘ ëœë¤ ì„ íƒ
  const items = getItemsByRarity(selectedRarity);
  return items[Math.floor(Math.random() * items.length)];
}
