// ë„íŠ¸ ì•„íŠ¸ SVG ìƒì„± í—¬í¼
function createPixelSvg(pixels: string[], colors: Record<string, string>, width: number = 16, height: number = 16): string {
  let svg = `<svg viewBox="0 0 ${width} ${height}" xmlns="http://www.w3.org/2000/svg" style="image-rendering: pixelated;">`;
  pixels.forEach((row, y) => {
    row.split("").forEach((char, x) => {
      if (char !== "." && colors[char]) {
        svg += `<rect x="${x}" y="${y}" width="1" height="1" fill="${colors[char]}"/>`;
      }
    });
  });
  svg += "</svg>";
  return svg;
}

